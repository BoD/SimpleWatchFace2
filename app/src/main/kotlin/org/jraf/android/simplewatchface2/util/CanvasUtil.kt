@file:Suppress("NOTHING_TO_INLINE")

package org.jraf.android.simplewatchface2.util

import android.graphics.Canvas
import android.graphics.Paint

inline fun Canvas.drawCenteredRect(cx: Float, cy: Float, width: Int, height: Int, paint: Paint) = drawRect(
    cx - width / 2F,
    cy - height / 2F,
    cx + width / 2F,
    cy + height / 2F,
    paint
)
